/*------------------------------------------------
                    检测忙位
------------------------------------------------*/
void Check_Busy()
{  
    RS=0;
    RW=1;
    E=1;
    DataPort=0xff;
    while((DataPort&0x80)==0x80);//忙则等待
    E=0;
}
/*------------------------------------------------
                   写命令
------------------------------------------------*/
void Write_Cmd(unsigned char Cmd)
{
	Check_Busy();
	RS=0;
	RW=0;
	E=1;
	DataPort=Cmd;
	DelayUs2x(5);
	E=0;
	DelayUs2x(5);
}
/*------------------------------------------------
                    写数据
------------------------------------------------*/
void Write_Data(unsigned char Data)
{
	Check_Busy();
	RS=1;
	RW=0;
	E=1;
	DataPort=Data;
	DelayUs2x(5);
	E=0;
	DelayUs2x(5);
}
/*------------------------------------------------
                   液晶屏初始化
------------------------------------------------*/
void Init_ST7920()
{  
    DelayMs(50);            
    Write_Cmd(0x30);   //基本指令集
    DelayMs(200);
    Write_Cmd(0x02);   // 地址归位
    DelayUs2x(200);
    Write_Cmd(0x0c);   //整体显示打开,游标关闭
    DelayUs2x(200);
    Write_Cmd(0x06);   //游标右移
    DelayUs2x(200);
    Write_Cmd(0x80);   //设定显示的起始地址
    DelayUs2x(200);
    Write_Cmd(0x01);   //清除显示
}
